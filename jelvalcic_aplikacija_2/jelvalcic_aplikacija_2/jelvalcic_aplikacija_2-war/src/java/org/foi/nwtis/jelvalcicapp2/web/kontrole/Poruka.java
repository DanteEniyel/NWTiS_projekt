package org.foi.nwtis.jelvalcicapp2.web.kontrole;

import java.util.Date;
import javax.mail.Flags;

/**
 *
 * @author jelvalcic
 * Klasa za poruke, malo prepravljena kako bi primala i sadr≈æaj poruke
 */
public class Poruka {
    private String id;
    private Date vrijeme;
    private String salje;
    private String predmet;
    private String vrsta;
    private int velicina;
    private Flags zastavice;
    private boolean brisati;
    private boolean procitano;
    private String tekstPoruke;

    public Poruka(String id, Date poslano, String salje, String predmet, String vrsta, int velicina, Flags zastavice, boolean brisati, boolean procitano, String tekstPoruke) {
        this.id = id;
        this.vrijeme = poslano;
        this.salje = salje;
        this.predmet = predmet;
        this.vrsta = vrsta;
        this.velicina = velicina;
        this.zastavice = zastavice;
        this.brisati = brisati;
        this.procitano = procitano;
        this.tekstPoruke = tekstPoruke;
    }

    public String getId() {
        return id;
    }

    public boolean isBrisati() {
        return brisati;
    }

    public void setBrisati(boolean brisati) {
        this.brisati = brisati;
    }

    public Flags getZastavice() {
        return zastavice;
    }

    public Date getVrijeme() {
        return vrijeme;
    }

    public String getPredmet() {
        return predmet;
    }

    public boolean isProcitano() {
        return procitano;
    }

    public void setProcitano(boolean procitano) {
        this.procitano = procitano;
    }
    
    public String getSalje() {
        return salje;
    }

    public String getVrsta() {
        return vrsta;
    }

    public int getVelicina() {
        return velicina;
    }

    public void setVrijeme(Date vrijeme) {
        this.vrijeme = vrijeme;
    }

    public String getTekstPoruke() {
        return tekstPoruke;
    }

    public void setTekstPoruke(String tekstPoruke) {
        this.tekstPoruke = tekstPoruke;
    }
  
}
